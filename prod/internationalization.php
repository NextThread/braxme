function InternationalizeDate($date){
    if($_SESSION['language']=='english'){
        return $date;
    }
    if($date == ''){
        return "";
    }
    $timepart = explode(" ",$date);
    $datepart = explode("/",$timepart[0]);
    $year = "";
    if(isset($datepart[2])){
        $year = $datepart[2];
    }
    if($year!=''){
        $year = '/'.$year;
    }
    if( !isset($timepart[1])){
        $timepart[1]='';
    }
    $newdate = $datepart[1]."/".$datepart[0].$year." ".$timepart[1];
    return $newdate;
}
